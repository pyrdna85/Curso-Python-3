"""
Tipos de dados:
str - string: Textos 'Assim' ou "Assim"
int - inteiro: Podem ser negativos ou positivos e nunca tem pontos 123456 ou 12
float - real/flutuante: Podem ser negativos ou positivos que requerem a utilização do ponto 10.50 ou 9.78
bool - booleano/lógico: Resultado positivo ou negativo True ou False
"""

print('Luiz', type('Luiz'))
print(1, type(1))
print(1.25, type(1.25))
print(10 == 10, type(10 == 10))

